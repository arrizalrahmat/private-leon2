const App = () => {
  const [count, setCount] = useState(0);
  const [isEven, setIsEven] = useState(true);

  const handleButtonPress = () => {
    setCount(count + 1);
  };

  useEffect(() => {
    console.log('aku ke render');
  }, []);

  useEffect(() => {
    console.log('watcher count triggered');
    if (count % 2 === 0) setIsEven(true);
    else setIsEven(false);
  }, [count]);

  useEffect(() => {
    if (isEven) console.log('tombolnya hijau');
    else console.log('tombolnya merah');
  }, [isEven]);

  return (
    <div className="App">
      <h1>Hello world</h1>
      <Button
        text="Pencet aku"
        counter={count}
        onClick={handleButtonPress}
        customContainer={{
          backgroundColor: isEven ? '#4cd137' : '#e84118',
          color: 'blue',
        }}
        isEven={isEven}
      />
    </div>
  );
};

export default App;
